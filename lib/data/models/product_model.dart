class ProductModel {
  final String name;
  final String descriptions;
  final String image;
  final double rating;
  final int ratingTop;
  final int ratingCount;
  final String tags;

  ProductModel({
    required this.name,
    required this.descriptions,
    required this.image,
    required this.rating,
    required this.ratingTop,
    required this.ratingCount,
    required this.tags,
  });
}
